/*
Program Name:  TenLoops
Inputs Required: no     
Variables/Types: int counter 
If/else statements: no 
Loops: yes
Loop how many times: 10
*/

public class TenLoops{
  public static void main(String[] args){
   
    int counter = 1;
    while(counter <= 10){
        System.out.println("Loop counter: "+counter);
        counter++;
    }
  }
}